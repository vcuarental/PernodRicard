function ASI_SG_AssignfieldConfig(fieldItem, preReservedData) {

    if (fieldItem.APIName == 'ASI_MFM_FOC_Type__c') {
        fieldItem.values = ASI_MFM_FOC_Type_Picklist();
    } else if (fieldItem.APIName == 'ASI_MFM_SG_A_P_Segment__c') {
        fieldItem.values = ASI_MFM_SG_A_P_Segment_Picklist();
    } else if (fieldItem.APIName == 'ASI_MFM_Customer_Name__c' && fieldItem.type == 'lookup') {
        if (fieldItem.additionalFilter.includes("{") && fieldItem.additionalFilter.includes("}")) {
            var location = preReservedData.header[0]['ASI_MFM_Prefix__r']['ASI_MFM_Location__c'];
            // str.substring(0,str.indexOf('{')) +'XXX'+str.substring(str.indexOf('}')+1,str.length)
            var filter = fieldItem.additionalFilter;
            if (location == 'Singapore') {
                fieldItem.additionalFilter = filter.substring(0, filter.indexOf('{')) + 'SG' + filter.substring(filter.indexOf('}') + 1, filter.length);
            } else if (location == 'Cambodia') {
                fieldItem.additionalFilter = filter.substring(0, filter.indexOf('{')) + 'CA' + filter.substring(filter.indexOf('}') + 1, filter.length);
            } else if (location == 'Myanmar') {
                fieldItem.additionalFilter = filter.substring(0, filter.indexOf('{')) + 'MM' + filter.substring(filter.indexOf('}') + 1, filter.length);
            } else if (location == 'Laos') {
                fieldItem.additionalFilter = filter.substring(0, filter.indexOf('{')) + 'LA' + filter.substring(filter.indexOf('}') + 1, filter.length);
            }
        }
        console.log(fieldItem.additionalFilter);
    } else if (fieldItem.APIName == 'ASI_MFM_A_C_Code__c' && fieldItem.type == 'lookup') {
        var filter = fieldItem.additionalFilter;
        var location = preReservedData.header[0]['ASI_MFM_Prefix__r']['ASI_MFM_Location__c'];
        fieldItem.additionalFilter = filter.substring(0, filter.indexOf('{')) + location + filter.substring(filter.indexOf('}') + 1, filter.length);
    }

    return fieldItem;
}

